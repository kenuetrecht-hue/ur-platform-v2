/**
 * Validate AI content for accuracy
 */
export function validateAIContent(content: AICreatorContent): {
  isValid: boolean;
  confidence: number;
  warnings: string[];
} {
  const warnings: string[] = [];

  if (!content.confidence || content.confidence < 0.7) {
    warnings.push("Low confidence score - verify with multiple sources");
  }

  if (!content.sources || content.sources.length === 0) {
    warnings.push("No sources provided - cross-reference information");
  }

  if (content.sentiment === "negative" && !content.sources) {
    warnings.push("Negative sentiment without sources - use caution");
  }

  return {
    isValid: warnings.length === 0,
    confidence: content.confidence || 0.5,
    warnings,
  };
}
